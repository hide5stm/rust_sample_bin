
fn main() {
    let a = ['0', '1', '2', '3', '4'];

    // 糖衣構文と実際の範囲の対応
    assert_eq!(a[ ..  ], ['0', '1', '2', '3', '4'] );
    assert_eq!(a[ .. 3], ['0', '1', '2',         ] );
    assert_eq!(a[ ..=3], ['0', '1', '2', '3'     ] );
    assert_eq!(a[1..  ], [     '1', '2', '3', '4'] );
    assert_eq!(a[1.. 3], [     '1', '2'          ] );
    assert_eq!(a[1..=3], [     '1', '2', '3'     ] );
}
